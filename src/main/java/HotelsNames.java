
public class HotelsNames {

    private String hotelName;
    private String hotelDescription;
    private String hotelLocation;
    private String hotelRating;
    private String hotelServices;
    private String hotelComments;





    public HotelsNames(String hotelName, String hotelDescription, String hotelLocation, String hotelRating, String hotelServices, String hotelComments){
        this.hotelName = hotelName;
        this.hotelDescription = hotelDescription;
        this.hotelLocation = hotelLocation;
        this.hotelRating = hotelRating;
        this.hotelServices = hotelServices;
        this.hotelComments = hotelComments;
    }

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getHotelDescription() {
        return hotelDescription;
    }

    public void setHotelDescription(String hotelDescription) {
        this.hotelDescription = hotelDescription;
    }

    public String getHotelLocation() {
        return hotelLocation;
    }

    public void setHotelLocation(String hotelLocation) {
        this.hotelLocation = hotelLocation;
    }

    public String getHotelRating() {
        return hotelRating;
    }

    public void setHotelRating(String hotelRating) {
        this.hotelRating = hotelRating;
    }

    public String getHotelServices() {
        return hotelServices;
    }

    public void setHotelServices(String hotelServices) {
        this.hotelServices = hotelServices;
    }

    public String getHotelComments() {
        return hotelComments;
    }

    public void setHotelComments(String hotelComments) {
        this.hotelComments = hotelComments;
    }
}



